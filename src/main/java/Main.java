public class Main {
    public static void main(String[] args) {
        byte  b = 13;
        short s = 123;
        int   i = 1344;
        long  l = 494949;

        float f = 13.3f;
        double d = 341.3;

        System.out.println("s - b = " + (s - b));
        System.out.println("i * l = " + (i * l));
        System.out.println("d / f = " + (d / f));
        System.out.println("d + l = " + (d + l));
    }
}
